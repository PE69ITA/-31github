import random

def выбор_компьютера():
    """Функция для выбора хода компьютера."""
    ходы = ['камень', 'ножницы', 'бумага']
    return random.choice(ходы)

def определить_победителя(игрок, компьютер):
    """Функция для определения победителя."""
    if игрок == компьютер:
        return "Ничья!"
    elif (
        (игрок == 'камень' and компьютер == 'ножницы') or
        (игрок == 'ножницы' and компьютер == 'бумага') or
        (игрок == 'бумага' and компьютер == 'камень')
    ):
        return "Вы победили!"
    else:
        return "Вы проиграли!"

def играть():
    """Основная функция для запуска игры."""
    print("Добро пожаловать в игру 'камень-ножницы-бумага'!")
    
    while True:
        игрок = input("Ваш выбор (камень/ножницы/бумага): ").lower()
        if игрок not in ['камень', 'ножницы', 'бумага']:
            print("Неверный выбор. Попробуйте снова.")
            continue
        
        компьютер = выбор_компьютера()
        print(f"Компьютер выбрал: {компьютер}")
        
        результат = определить_победителя(игрок, компьютер)
        print(результат)
        
        продолжить = input("Хотите сыграть еще раз? (да/нет): ").lower()
        if продолжить != 'да':
            break

if __name__ == "__main__":
    играть()
